// Translate task name
export const getTranslatedTaskName = (taskName, language = 'en') => {
  if (!taskName || language === 'en') return taskName;
  
  // Japanese translations for specific tasks
  if (language === 'ja') {
    // Common task patterns
    if (taskName.includes("Schedule a meeting")) {
      return taskName.replace("Schedule a meeting", "ミーティングをスケジュールする");
    }
    if (taskName.includes("Create a list")) {
      return taskName.replace("Create a list", "リストを作成する");
    }
    if (taskName.includes("Research")) {
      return taskName.replace("Research", "調査する");
    }
    if (taskName.includes("Identify")) {
      return taskName.replace("Identify", "特定する");
    }
    
    // Specific tasks
    switch (taskName) {
      case "Schedule a 1-hour meeting with your manager to discuss career path opportunities":
        return "キャリアパスの機会について話し合うために、マネージャーと1時間のミーティングをスケジュールする";
      case "Research 3 specific skills that people in your target position possess":
        return "目標とするポジションの人々が持つ3つの特定のスキルを調査する";
      case "Email your manager requesting to present at the next team or department meeting":
        return "次のチームまたは部門のミーティングでプレゼンテーションを行うことをマネージャーにメールで依頼する";
      case "Identify and reach out to a potential mentor with a specific request for guidance":
        return "潜在的なメンターを特定し、具体的な指導の要請をする";
      case "Configure 'do not disturb' settings on your phone and computer for non-work hours":
        return "非勤務時間のために、電話とコンピューターの「邪魔しないで」設定を構成する";
      case "Draft an email to colleagues outlining your standard work hours and response times":
        return "標準的な勤務時間と応答時間を概説する同僚へのメールを下書きする";
      case "Block 30-minute chunks in your calendar for focused work on high-priority tasks":
        return "高優先度のタスクに集中して取り組むために、カレンダーに30分のブロックを設定する";
      case "Create a 10-minute end-of-workday routine to prepare for tomorrow":
        return "明日の準備のために、10分の仕事終わりのルーティンを作成する";
      default:
        return taskName;
    }
  }
  
  // Default to original task name
  return taskName;
};